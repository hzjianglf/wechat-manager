	/**
	 * 判断字符是否是中文字符
	 * 
	 * @param c
	 *            待判断的字符
	 * @return 是否是中文汉字
	 */
	public static boolean isChinese(char c) {
		for (int[] u : UNICODE) {
			if (c >= u[0] && c <= u[1])
				return true;
		}
		return false;
	}

	/**
	 * 字符是否有拼音
	 * 
	 * @param c
	 *            待判断的字符
	 * @return 是否有拼音
	 */
	public static boolean hasPinYin(char c) {
		for (int[] u : range) {
			if (c < u[0])
				return false;
			if ((u.length == 1 && c == u[0]) || (u.length == 2 && c >= u[0] && c <= u[1]))
				return true;
		}
		return false;
	}

	/**
	 * 将字母替换成拼音符号
	 * 
	 * @param str
	 *            英文字母
	 * @return 拼音符号
	 */
	public static String abc2py(String str) {
		String old = str.replace('v', 'ü');
		StringBuffer sb = new StringBuffer();
		for (int f = 0; f < ABC.length; f++)
			for (int i = 0; i < old.length(); i++) {
				if (ABC[f] == old.toCharArray()[i]) {
					if (i > 0)
						sb.append(old.substring(0, i));
					sb.append(YB[f].toCharArray()[Integer.parseInt(old.substring(old.length() - 1)) - 1]);
					if (i < old.length() - 2)
						sb.append(old.substring(i + 1, old.length() - 1));
					return sb.toString();
				}
			}
		return "";
	}

	/**
	 * 半角转换
	 * 
	 * @param str
	 *            文字
	 * @return 输出
	 */
	public static String SBCchange(String str) {
		String outStr = "";
		String Tstr = "";
		byte[] b = null;

		for (int i = 0; i < str.length(); i++) {
			try {
				Tstr = str.substring(i, i + 1);
				b = Tstr.getBytes("unicode");
			} catch (UnsupportedEncodingException e) {
				e.printStackTrace();
			}

			if (b[3] == -1) {
				b[2] = (byte) (b[2] + 32);
				b[3] = 0;

				try {
					outStr = outStr + new String(b, "unicode");
				} catch (UnsupportedEncodingException e) {
					e.printStackTrace();
				}
			} else
				outStr = outStr + Tstr;
		}

		return outStr;
	}

	public static boolean check(String regex, String value) {
		Pattern p = Pattern.compile(regex);
		Matcher m = p.matcher(value);
		return m.matches();
	}

	public static boolean checkEmail(String email) {
		return check(CHECK_EMAIL_REGEX, email);
	}

	public static boolean checkMobile(String mobiles) {
		return check(CHECK_MOBILE_REGEX, mobiles);
	}

	public static boolean checkUserName(String name) {
		return check(CHECK_USERNAME_REGEX, name);
	}

	public static boolean notNull(String name) {
		if (null != name && !name.trim().equals("")) {
			return true;
		}
		return false;
	}

